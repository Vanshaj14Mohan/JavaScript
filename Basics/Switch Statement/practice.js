//Use Switch Statement to print the day of the week using a number variable "day" with values 1-7, 1 = Monday, 2 = Tuesday....
let day = "5";
switch(day){
    case "1":
        console.log("It's Monday");
        break;
    case "2":
        console.log("It's Tuesday");
        break;
    case "3":
        console.log("It's Wednesday");
        break;
    case "4":
        console.log("It's Thursday");
        break;
    case "5":
        console.log("It's Friday");
        break;
    case "6":
        console.log("It's Saturday");
        break;
    case "7":
        console.log("It's Sunday");
        break;
    default:
        console.log("Out of range");
}
